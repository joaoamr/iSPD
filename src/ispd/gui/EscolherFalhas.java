    private void gerarAleatorio(){
        int p = (Integer)lifetimespinner.getValue();
        double life = (double)p;
        p = (Integer)lengthspinner.getValue();
        double length = (double)p;
        p = (Integer)nodesspinner.getValue();
        int n = p;
        falhaprocessamento = new FalhaExponencial(life, length, n);
        
        p = (Integer)lifetimespinner1.getValue();
        life = (double)p;
        p = (Integer)lengthspinner1.getValue();
        length = (double)p;
        p = (Integer)nodesspinner1.getValue();
        n = p;
        falhacomunicacao = new FalhaExponencial(life, length, n);
        
        setVisible(false);
    }
    
