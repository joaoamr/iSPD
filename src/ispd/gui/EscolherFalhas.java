    public void update(DesenhoGrade aDesenho){
        this.aDesenho = aDesenho;
        Machine procs[] = new Machine[aDesenho.getVertices().size()];
        Link links[] = new Link[aDesenho.getArestas().size()];
       
        for(int i = 0; i < procs.length; i++){
            procs[i] = (Machine) aDesenho.getVertices().toArray()[i];
        }
       
        for(int i = 0; i < links.length; i++){
            links[i] = (Link) aDesenho.getArestas().toArray()[i];
        }
       
        DefaultTableModel model = (DefaultTableModel) failTable.getModel();
       
        while(failTable.getRowCount() > 0)
            model.removeRow(0);
       
        for(int i = 0 ; i < procs.length; i++){
            int t[] = maquinasfalhadas.get(procs[i].getId());
            Object[] linha = new Object[3];
            linha[0] = procs[i].getId().getNome();
            if(t == null){
               linha[1] = " - ";
               linha[2] = " - ";
            }else{
               linha[1] = t[0];
               linha[2] = t[1];
            }
            model.addRow(linha);
        }
        
        for(int i = 0 ; i < links.length; i++){
            int t[] = maquinasfalhadas.get(links[i].getId());
            Object[] linha = new Object[3];
            linha[0] = links[i].getId().getNome();
            if(t == null){
               linha[1] = " - ";
               linha[2] = " - ";
            }else{
               linha[1] = t[0];
               linha[2] = t[1];
            }
            model.addRow(linha);
        }
        
    }
    
